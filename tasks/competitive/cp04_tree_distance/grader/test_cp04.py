# ---------------------------------------------------------------------------
# 3. Path 0-1-2 with unit weights
# ---------------------------------------------------------------------------

def test_path_unweighted():
    # 0 --1-- 1 --1-- 2
    # res[0] = 1+2 = 3; res[1] = 1+1 = 2; res[2] = 2+1 = 3
    edges = [(0, 1, 1), (1, 2, 1)]
    result = sum_of_distances(3, edges)
    expected = _reference_sum_of_distances(3, edges)
    assert result == expected, f"expected {expected}, got {result}"


# ---------------------------------------------------------------------------
# 4. Path 0-1-2 with non-uniform weights
# ---------------------------------------------------------------------------

def test_path_weighted():
    # 0 --1-- 1 --2-- 2
    # res[0] = 1+3 = 4; res[1] = 1+2 = 3; res[2] = 3+2 = 5
    edges = [(0, 1, 1), (1, 2, 2)]
    result = sum_of_distances(3, edges)
    expected = _reference_sum_of_distances(3, edges)
    assert result == expected, f"expected {expected}, got {result}"
    assert result == [4, 3, 5], f"exact check failed: {result}"


# ---------------------------------------------------------------------------
# 5. Star graph, unit weights
# ---------------------------------------------------------------------------

def test_star_unweighted():
    # Star: center=0, leaves=1,2,3,4 all weight 1
    # res[0] = 4; res[leaf] = 1 + 1*3 + 1*3... actually:
    # res[1] = dist(1,0)+dist(1,2)+dist(1,3)+dist(1,4) = 1+2+2+2 = 7 (wait n=5 here)
    n = 5
    edges = [(0, i, 1) for i in range(1, n)]
    result = sum_of_distances(n, edges)
    expected = _reference_sum_of_distances(n, edges)
    assert result == expected, f"expected {expected}, got {result}"
    # center: sum = 4*1 = 4
    assert result[0] == 4, f"center should have distance sum 4, got {result[0]}"
    # each leaf: 1 (to center) + 2*(n-2) (to other leaves via center)
    for i in range(1, n):
        expected_leaf = 1 + 2 * (n - 2)
        assert result[i] == expected_leaf, f"leaf {i} expected {expected_leaf}, got {result[i]}"


