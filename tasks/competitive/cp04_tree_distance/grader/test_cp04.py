# ---------------------------------------------------------------------------
# Reference implementation: O(n^2) Dijkstra from each node (correct, used
# for verifying small cases inside the grader).
# ---------------------------------------------------------------------------

def _reference_sum_of_distances(n: int, edges: list[tuple]) -> list[int]:
    """Naive O(n^2 log n) reference — correct but slow; used for small n only."""
    if n == 1:
        return [0]
    adj: list[list[tuple[int, int]]] = [[] for _ in range(n)]
    for u, v, w in edges:
        adj[u].append((v, w))
        adj[v].append((u, w))

    def dijkstra_sum(src: int) -> int:
        dist = [float("inf")] * n
        dist[src] = 0
        heap = [(0, src)]
        while heap:
            d, u = heapq.heappop(heap)
            if d > dist[u]:
                continue
            for v, w in adj[u]:
                nd = d + w
                if nd < dist[v]:
                    dist[v] = nd
                    heapq.heappush(heap, (nd, v))
        return int(sum(d for d in dist if d != float("inf")))

    return [dijkstra_sum(i) for i in range(n)]


# ---------------------------------------------------------------------------
# Helper: build a random tree with fixed seed
# ---------------------------------------------------------------------------

def _random_tree(n: int, seed: int, max_w: int = 100) -> list[tuple]:
    """Build a random tree on n nodes with given seed. Returns list of (u,v,w)."""
    rng = random.Random(seed)
    edges = []
    for v in range(1, n):
        u = rng.randint(0, v - 1)
        w = rng.randint(1, max_w)
        edges.append((u, v, w))
    return edges


