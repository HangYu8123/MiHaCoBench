# ---------------------------------------------------------------------------
# 10. Caterpillar/path tree n=1000 — medium adversarial (checks iterative DFS)
# ---------------------------------------------------------------------------

def test_caterpillar_medium():
    """Path (spine) of length 500 with one leaf per spine node."""
    spine = 500
    n = spine * 2  # 1000 nodes: 0..499 spine, 500..999 leaves
    # Build spine: 0-1-2-...-499
    edges = [(i, i + 1, 1) for i in range(spine - 1)]
    # Attach one leaf to each spine node
    for i in range(spine):
        edges.append((i, spine + i, 2))
    result = sum_of_distances(n, edges)
    expected = _reference_sum_of_distances(n, edges)
    assert result == expected, "caterpillar medium: mismatch vs reference"


# ---------------------------------------------------------------------------
# 11. ADVERSARIAL hard time gate: n=200000, fixed seed, timeout=15s
# ---------------------------------------------------------------------------

@pytest.mark.adversarial
def test_adversarial_time_gate():
    """n=200000 random tree (fixed seed), must complete within 15 seconds.

    The O(n) rerooting DP finishes well within the budget.
    The O(n^2) naive approach (Dijkstra from each node) would require
    ~2 × 10^10 steps and takes many minutes — it TIMES OUT here.
    """
    n = 200_000
    edges = _random_tree(n, seed=42, max_w=100)
    result = gu.run_within(15.0, sum_of_distances, n, edges)
    # Basic sanity: correct length, non-negative integers
    assert isinstance(result, list), "result must be a list"
    assert len(result) == n, f"expected {n} elements, got {len(result)}"
    assert all(isinstance(x, int) and x >= 0 for x in result), \
        "all elements must be non-negative integers"
    # Spot-check a few nodes against reference on a small subtree
    # (build same tree n=10 with same initial edges for sanity)
    n_small = 50
    edges_small = _random_tree(n_small, seed=42, max_w=100)
    ref_small = _reference_sum_of_distances(n_small, edges_small)
    result_small = sum_of_distances(n_small, edges_small)
    assert result_small == ref_small, "spot check on small instance failed"


