# 12. SOFT complexity signal
# ---------------------------------------------------------------------------

@pytest.mark.soft_complexity
def test_soft_complexity():
    """Empirical time-complexity estimate -- advisory; fails only if >2 tiers off O(n).

    Sizes are kept small so even an O(n^2) submission can complete during the
    grader run; the hard gate (test_adversarial_time_gate) is the real
    complexity discriminator.  The soft check only rejects algo far worse than
    O(n^2) (e.g. O(n^3) or exponential).
    """
    # Sizes small enough that O(n^2) finishes in < 0.5s per size
    sizes = [100, 200, 300, 400, 500]

    def make_input(n: int):
        return (n, _random_tree(n, seed=n, max_w=10))

    timings = gu.measure_runtime(
        make_input,
        lambda args: sum_of_distances(args[0], args[1]),
        sizes,
        repeats=2,
    )
    report = gu.estimate_time_complexity(timings)
    label = report["label"]
    print(f"[soft_complexity] estimated={label}  target=O(n)  ranked={report['ranked'][:3]}")
    # Allow up to O(n^2) — two tiers above O(n) — before failing.
    # The adversarial gate is the real discriminator.
    assert gu.within_one_tier(label, "O(n^2)"), (
        f"soft complexity check: estimated {label} is more than one tier above O(n^2). "
        "This is a strong signal of an incorrect (or worse-than-quadratic) algorithm."
    )


