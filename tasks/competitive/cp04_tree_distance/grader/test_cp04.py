@pytest.mark.parametrize("case", _CORPUS["cases"], ids=range(len(_CORPUS["cases"])))
def test_mutation_corpus(case):
    """Replay the oracle-grounded, mutation-seeded corpus.

    Expected outputs come from an independent all-pairs Dijkstra oracle,
    cross-validated against the gold; each input discriminates a real wrong
    solution. A subtly-incorrect candidate that slips past the curated tests is
    caught here by an empirically-discovered killing input.
    """
    n, raw_edges = case["args"]
    edges = [tuple(e) for e in raw_edges]
    assert sum_of_distances(n, edges) == case["expected"]
