# ---------------------------------------------------------------------------
# Small reference implementation used only in the grader for cross-checking
# ---------------------------------------------------------------------------

def _naive(text: str, pattern: str) -> list[int]:
    """Naive O(n*m) reference — only used on small inputs in this grader."""
    n, m = len(text), len(pattern)
    if m == 0 or m > n:
        return []
    results = []
    for i in range(n - m + 1):
        if all(p == '?' or p == t for p, t in zip(pattern, text[i:i + m])):
            results.append(i)
    return results


# ---------------------------------------------------------------------------
# 1  Empty pattern → []
# ---------------------------------------------------------------------------

def test_empty_pattern():
    assert count_pattern("hello world", "") == []
    assert count_pattern("", "") == []


# ---------------------------------------------------------------------------
# 2  Pattern longer than text → []
# ---------------------------------------------------------------------------

def test_pattern_longer_than_text():
    assert count_pattern("ab", "abc") == []
    assert count_pattern("", "a") == []


# ---------------------------------------------------------------------------
# 3  No match
# ---------------------------------------------------------------------------

def test_no_match():
    result = count_pattern("abcdef", "xyz")
    assert result == [], f"expected [], got {result}"
    result2 = count_pattern("aababab", "aaa")
    assert result2 == _naive("aababab", "aaa")


# ---------------------------------------------------------------------------
# 4  Match at start of text
# ---------------------------------------------------------------------------

def test_single_match_start():
    result = count_pattern("abcdef", "abc")
    assert result == [0], f"expected [0], got {result}"


# ---------------------------------------------------------------------------
# 5  Match at end of text
# ---------------------------------------------------------------------------

def test_single_match_end():
    result = count_pattern("xyzabc", "abc")
    assert result == [3], f"expected [3], got {result}"


# ---------------------------------------------------------------------------
# 6  Multiple non-overlapping matches
# ---------------------------------------------------------------------------

def test_multiple_matches():
    result = count_pattern("abcabc", "abc")
    assert result == [0, 3], f"expected [0, 3], got {result}"


# ---------------------------------------------------------------------------
# 7  Overlapping matches
# ---------------------------------------------------------------------------

def test_overlapping_matches():
    # 'aaaa' contains 'aa' at positions 0, 1, 2
    result = count_pattern("aaaa", "aa")
    assert result == [0, 1, 2], f"expected [0,1,2], got {result}"

    # Classic KMP overlapping: 'aaaaaa' / 'aaa' → [0,1,2,3]
    result2 = count_pattern("aaaaaa", "aaa")
    assert result2 == _naive("aaaaaa", "aaa"), f"overlapping mismatch: {result2}"


# ---------------------------------------------------------------------------
# 8  Single wildcard '?' matches any character
# ---------------------------------------------------------------------------

def test_single_wildcard():
    result = count_pattern("abcabc", "a?c")
    assert result == [0, 3], f"expected [0, 3], got {result}"

    # '?' in middle
    result2 = count_pattern("aXbYc", "a?b")
    assert result2 == _naive("aXbYc", "a?b")

    # '?' at the start
    result3 = count_pattern("xyzxyz", "?yz")
    assert result3 == _naive("xyzxyz", "?yz")

    # '?' does NOT match zero chars
    assert count_pattern("ac", "a?c") == []


# ---------------------------------------------------------------------------
# 9  All-wildcard pattern matches exactly once at each valid position
# ---------------------------------------------------------------------------

def test_all_wildcard_pattern():
    # pattern '?????' has length 5, text 'hello' has length 5 → [0]
    result = count_pattern("hello", "?????")
    assert result == [0], f"expected [0], got {result}"

    # pattern '??' in 'abcd' → [0, 1, 2]
    result2 = count_pattern("abcd", "??")
    assert result2 == _naive("abcd", "??"), f"all-wildcard mismatch: {result2}"


# ---------------------------------------------------------------------------
# 10  Matches at both start and end
# ---------------------------------------------------------------------------

def test_match_start_and_end():
    result = count_pattern("abXYZab", "ab")
    assert result == [0, 5], f"expected [0, 5], got {result}"


# ---------------------------------------------------------------------------
# 11  Wildcard at boundaries of pattern
# ---------------------------------------------------------------------------

def test_wildcard_at_boundaries():
    # '?b?' matches any char + 'b' + any char
    text = "abcabcXbZ"
    result = count_pattern(text, "?b?")
    expected = _naive(text, "?b?")
    assert result == expected, f"wildcard boundary: {result} != {expected}"


# ---------------------------------------------------------------------------
# 12  Single character text and pattern
# ---------------------------------------------------------------------------

def test_single_char_text_and_pat():
    assert count_pattern("a", "a") == [0]
    assert count_pattern("a", "b") == []
    assert count_pattern("a", "?") == [0]


def test_special_chars_in_text():
    """'?' in the pattern matches ANY character in the text, including
    punctuation such as '#'. The contract does not restrict the alphabet, so a
    wildcard must match a literal '#' the same as any other character (a common
    sentinel-collision bug fails this)."""
    assert count_pattern("a##b", "a??b") == _naive("a##b", "a??b") == [0]
    assert count_pattern("x#y#z", "?#?") == _naive("x#y#z", "?#?")
    assert count_pattern("a#b#a#b", "a#b") == _naive("a#b#a#b", "a#b")


# ---------------------------------------------------------------------------
# 13  HARD adversarial complexity gate
#     text='a'*1_000_000, pattern='a'*499+'?'
#     Naive O(n*m) needs ~5e11 comparisons → times out.
#     KMP/Z-algo O(n+m) finishes in <1 second.
# ---------------------------------------------------------------------------

@pytest.mark.adversarial_complexity
def test_adversarial_complexity_gate():
    """Degenerate input that defeats naive O(n*m) backtracking."""
    K = 1_000_000
    m_pat = 499
    text = "a" * K
    pattern = "a" * m_pat + "?"
    # Must complete within 5 seconds; naive O(n*m) cannot.
    result = gu.run_within(5.0, count_pattern, text, pattern)
    # Verify correctness: pattern matches at every position 0..K-m_pat-1
    expected_len = K - (m_pat + 1) + 1  # = K - m_pat
    assert isinstance(result, list), "result must be a list"
    assert len(result) == expected_len, (
        f"expected {expected_len} matches, got {len(result)}"
    )
    assert result[0] == 0
    assert result[-1] == K - m_pat - 1


