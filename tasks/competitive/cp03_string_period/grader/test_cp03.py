# 14  SOFT complexity signal (only fails if far worse than O(n))
# ---------------------------------------------------------------------------

@pytest.mark.soft_complexity
def test_soft_complexity():
    """Empirical time-complexity estimate — advisory; fails only if egregiously wrong."""
    import random
    import string
    import time

    # Measure time for count_pattern on text of varying lengths
    # Use a worst-case-ish pattern but keep sizes manageable
    sizes = [10_000, 20_000, 40_000, 80_000, 160_000]

    def make_input(n):
        rng = random.Random(n)
        text = "".join(rng.choices("ab", k=n))
        # pattern: 'a'*10 + '?' to exercise the wildcard path
        pattern = "a" * 10 + "?"
        return text, pattern

    timings = gu.measure_runtime(
        make_input,
        lambda pair: count_pattern(*pair),
        sizes,
        repeats=2,
    )
    report = gu.estimate_time_complexity(timings)
    label = report["label"]
    print(
        f"[soft_complexity] estimated={label}  target=O(n)  ranked={report['ranked'][:4]}"
    )
    # Allow up to O(n log n) — one tier above O(n) — before failing
    assert gu.within_one_tier(label, "O(n)"), (
        f"soft complexity check: estimated {label} is more than one tier above O(n). "
        "Likely an O(n*m) implementation; use KMP or Z-algorithm."
    )


