# 13  SOFT complexity signal (advisory; fails only if >2 tiers worse than O(n log n))
# ---------------------------------------------------------------------------

@pytest.mark.soft_complexity
def test_soft_complexity():
    """Empirical time-complexity check — advisory only."""
    rng_outer = random.Random(2024)

    def make_input(n: int):
        rng = random.Random(n)
        ops = []
        for _ in range(n):
            l = rng.randint(0, n - 1)
            r = rng.randint(l, n - 1)
            if rng.random() < 0.5:
                ops.append(("add", l, r, rng.randint(-100, 100)))
            else:
                ops.append(("sum", l, r))
        return (n, ops)

    sizes = [500, 1000, 2000, 4000, 8000]
    timings = gu.measure_runtime(make_input, lambda args: process_queries(*args), sizes)
    report = gu.estimate_time_complexity(timings)
    label = report["label"]
    print(f"[soft_complexity] estimated={label}  target=O(n log n)  ranked={report['ranked'][:3]}")
    assert gu.within_one_tier(label, "O(n log n)"), (
        f"soft complexity check: estimated {label} is more than one tier above O(n log n)."
    )


