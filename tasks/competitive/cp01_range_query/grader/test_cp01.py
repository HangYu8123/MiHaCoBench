@pytest.mark.parametrize("case", _CORPUS["cases"], ids=range(len(_CORPUS["cases"])))
def test_mutation_corpus(case):
    """Replay the oracle-grounded, mutation-seeded corpus.

    Expected outputs come from a brute-force array simulation (independent
    oracle), cross-validated against the gold; each input discriminates a real
    wrong solution. A subtly-incorrect candidate that slips past the curated
    tests is caught here by an empirically-discovered killing input.

    JSON turns tuples into lists — convert each op back to a tuple before calling.
    """
    n, ops_raw = case["args"]
    ops = [tuple(o) for o in ops_raw]
    assert process_queries(n, ops) == case["expected"]


