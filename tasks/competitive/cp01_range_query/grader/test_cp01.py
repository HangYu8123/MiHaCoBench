def test_interleaved_ops():
    """Add and sum alternate — verify running state is maintained."""
    n = 7
    ops = [
        ("add", 0, 6, 1),
        ("sum", 0, 6),        # 7
        ("add", 0, 3, 10),
        ("sum", 0, 3),        # 4*(1+10) = 44
        ("sum", 4, 6),        # 3
        ("add", 2, 5, -5),
        ("sum", 0, 6),        # 1+11+6+6+1+1-5 ... use reference
    ]
    expected = _reference(n, ops)
    result = process_queries(n, ops)
    assert result == expected, f"expected {expected}, got {result!r}"


# ---------------------------------------------------------------------------
# 10  Correctness vs brute-force reference (random small ops)
# ---------------------------------------------------------------------------

def test_correctness_vs_ref():
    """Randomly generated small instances compared against brute-force reference."""
    rng = random.Random(99)
    for trial in range(20):
        n = rng.randint(2, 50)
        ops = []
        for _ in range(100):
            l = rng.randint(0, n - 1)
            r = rng.randint(l, n - 1)
            if rng.random() < 0.5:
                v = rng.randint(-100, 100)
                ops.append(("add", l, r, v))
            else:
                ops.append(("sum", l, r))
        expected = _reference(n, ops)
        got = process_queries(n, ops)
        assert got == expected, (
            f"trial {trial}: n={n}, expected {expected[:5]}..., got {got[:5]}..."
        )


# ---------------------------------------------------------------------------
# 11  Adversarial: interleaved overlapping range-adds then range-sums
# ---------------------------------------------------------------------------

def test_adversarial_ranges():
    """Correctness under dense, interleaved, overlapping operations on a moderate array."""
    rng = random.Random(7)
    n = 1000
    ops = []
    # Build a mix of overlapping adds and targeted sums
    for _ in range(500):
        l1 = rng.randint(0, n - 1)
        r1 = rng.randint(l1, min(l1 + 200, n - 1))
        v = rng.randint(-50, 50)
        ops.append(("add", l1, r1, v))
        l2 = rng.randint(0, n - 1)
        r2 = rng.randint(l2, min(l2 + 300, n - 1))
        ops.append(("sum", l2, r2))

    expected = _reference(n, ops)
    got = process_queries(n, ops)
    assert got == expected, (
        f"adversarial_ranges mismatch: first diff at index "
        f"{next(i for i,(a,b) in enumerate(zip(got,expected)) if a!=b)}"
        if got != expected else ""
    )


# ---------------------------------------------------------------------------
# 12  HARD time gate (adversarial large input, fixed seed 1337)
#     The broken O(n*q) naive solution MUST time out here.
#     A correct O((n+q) log n) solution (dual Fenwick BIT OR lazy segment
#     tree) MUST complete within 10 seconds.
# ---------------------------------------------------------------------------

def test_adversarial_hard_gate():
    """n=200000, q~200000 interleaved ops — must complete within 10 seconds.

    Seed: random.Random(1337).  An O(n*q) naive solution needs ~2x10^10
    iterations (many minutes). Both an O((n+q) log n) dual-Fenwick BIT and a
    lazy-propagation segment tree finish well inside the 10 s budget; the
    budget is deliberately generous so a correct segtree is not a false
    negative on slower hardware.
    """
    SEED = 1337
    n = 200_000
    q = 200_000
    rng = random.Random(SEED)

    ops = []
    for _ in range(q):
        l = rng.randint(0, n - 1)
        r = rng.randint(l, n - 1)
        if rng.random() < 0.5:
            v = rng.randint(-1000, 1000)
            ops.append(("add", l, r, v))
        else:
            ops.append(("sum", l, r))

    result = gu.run_within(10.0, process_queries, n, ops)
    # Must return a list of ints
    assert isinstance(result, list), f"expected list, got {type(result)}"
    assert all(isinstance(x, int) for x in result), "all sum results must be int"


