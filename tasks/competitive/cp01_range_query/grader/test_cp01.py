# ---------------------------------------------------------------------------
# Reference implementation (brute-force O(n*q)) — used ONLY in the grader for
# small-input correctness checks. It is never imported from a solution.
# ---------------------------------------------------------------------------

def _reference(n: int, ops: list[tuple]) -> list[int]:
    arr = [0] * n
    out: list[int] = []
    for op in ops:
        if op[0] == "add":
            _, l, r, v = op
            for i in range(l, r + 1):
                arr[i] += v
        else:
            _, l, r = op
            out.append(sum(arr[l : r + 1]))
    return out


